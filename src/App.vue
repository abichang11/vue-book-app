<script setup lang="ts">
import { getAuth, signInWithPopup, GoogleAuthProvider } from '@firebase/auth'

const signIn = (): void => {
  const auth = getAuth()
  const provider = new GoogleAuthProvider()
  signInWithPopup(auth, provider)
    .then((result) => {
      console.log(result)
    })
    .catch((error) => {
      console.log(error)
    })
}
</script>

<template>
  <button @click="signIn">Google</button>
</template>

